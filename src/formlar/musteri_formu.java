    private void btn_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guncelleActionPerformed
        DefaultTableModel model = (DefaultTableModel) tablo_musteri.getModel();
        musteri_crud cr = new musteri_crud();
        try {

            m.setMusteri_id((int) model.getValueAt(tablo_musteri.getSelectedRow(), 0));
            m.setMusteri_ad(txt_ad.getText());
            m.setMusteri_soyad(txt_soyad.getText());
            m.setMusteri_tc(Integer.parseInt(txt_tc.getText()));
            m.setMusteri_tel(Integer.parseInt(txt_gsm.getText()));
            m.setMusteri_email(txt_mail.getText());
            cr.guncelle(m);
            model.setValueAt(txt_ad.getText(), tablo_musteri.getSelectedRow(), 1);
            model.setValueAt(txt_soyad.getText(), tablo_musteri.getSelectedRow(), 2);
            model.setValueAt(txt_tc.getText(), tablo_musteri.getSelectedRow(), 3);
            model.setValueAt(txt_gsm.getText(), tablo_musteri.getSelectedRow(), 4);
            model.setValueAt(txt_mail.getText(), tablo_musteri.getSelectedRow(), 5);
            formu_temizle();
            kontrol();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "1.Tablodan Güncellenecek Müşteriyi Seçtiğinizden Emin Olun!\n2.Sayısal Değer İsteyen Alanlara Hatalı Giriş Yapmayın!", "Uyarı Penceresi", JOptionPane.INFORMATION_MESSAGE, i);

        }

    }//GEN-LAST:event_btn_guncelleActionPerformed

