    private void btn_kaydet_musteriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kaydet_musteriActionPerformed
        musteri_crud cr = new musteri_crud();
        try {

            m.setMusteri_ad(txt_ad.getText().trim());
            m.setMusteri_soyad(txt_soyad.getText().trim());
            m.setMusteri_email(txt_mail.getText().trim());
            m.setMusteri_tc(Integer.parseInt(txt_tc.getText().trim()));
            m.setMusteri_tel(Integer.parseInt(txt_gsm.getText().trim()));
            DefaultTableModel model = (DefaultTableModel) tablo_musteri.getModel();
            Object[] row = new Object[6];
            cr.ekle(m);
            row[1] = m.getMusteri_ad();
            row[2] = m.getMusteri_soyad();
            row[3] = m.getMusteri_tc();
            row[4] = m.getMusteri_tel();
            row[5] = m.getMusteri_email();
            model.addRow(row);
            tabloyu_temizle();
            musteri_goster();
            formu_temizle();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "1.Hiçbir Alanı Boş Bırakmadığınızdan Emin Olun!\n2.Sayısal Değer İsteyen Alanlara Hatalı Giriş Yapmayın!", "Uyarı Penceresi", JOptionPane.INFORMATION_MESSAGE, i);

        }
    }//GEN-LAST:event_btn_kaydet_musteriActionPerformed

    private void tablo_musteriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablo_musteriMouseClicked
        DefaultTableModel model = (DefaultTableModel) tablo_musteri.getModel();
        try {

            txt_ad.setText(model.getValueAt(tablo_musteri.getSelectedRow(), 1).toString());
            txt_soyad.setText(model.getValueAt(tablo_musteri.getSelectedRow(), 2).toString());
            txt_mail.setText(model.getValueAt(tablo_musteri.getSelectedRow(), 5).toString());
            txt_gsm.setText(model.getValueAt(tablo_musteri.getSelectedRow(), 4).toString());
            txt_tc.setText(model.getValueAt(tablo_musteri.getSelectedRow(), 3).toString());
            btn_kaydet_musteri.setVisible(false);
            btn_guncelle.setVisible(true);
            btn_temizle.setVisible(true);
            btn_silme.setVisible(true);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tablo_musteriMouseClicked

    private void btn_silmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_silmeActionPerformed
        musteri_crud cr = new musteri_crud();
        DefaultTableModel model = (DefaultTableModel) tablo_musteri.getModel();
        try {
            int a = JOptionPane.showConfirmDialog(this,
                    "Silmek İstediğine Emin misin?", "Doğrulama Penceresi",
                    JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, i);
            if (a == 0) {
                m.setMusteri_id((int) model.getValueAt(tablo_musteri.getSelectedRow(), 0));
                cr.sil(m);
                model.removeRow(tablo_musteri.getSelectedRow());
                model.fireTableDataChanged();
                formu_temizle();
                kontrol();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tablodan Silinecek Müşteriyi Seçtiğinizden Emin Olun!", "Uyarı Penceresi", JOptionPane.INFORMATION_MESSAGE, i);

        }
    }//GEN-LAST:event_btn_silmeActionPerformed

