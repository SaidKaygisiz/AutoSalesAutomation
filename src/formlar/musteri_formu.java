    public void musteri_goster() {
        musteri_crud cr = new musteri_crud();
        List<db_musteri> mL = cr.listele();
        DefaultTableModel model = (DefaultTableModel) tablo_musteri.getModel();
        tablo_musteri.setFillsViewportHeight(true);
        Object[] row = new Object[6];

        mL.stream().map((mL1) -> {
            row[0] = mL1.getMusteri_id();
            return mL1;
        }).map((mL1) -> {
            row[1] = mL1.getMusteri_ad();
            return mL1;
        }).map((mL1) -> {
            row[2] = mL1.getMusteri_soyad();
            return mL1;
        }).map((mL1) -> {
            row[3] = mL1.getMusteri_tc();
            return mL1;
        }).map((mL1) -> {
            row[4] = mL1.getMusteri_tel();
            return mL1;
        }).map((mL1) -> {
            row[5] = mL1.getMusteri_email();
            return mL1;
        }).forEach((_item) -> {
            model.addRow(row);
        });

    }

