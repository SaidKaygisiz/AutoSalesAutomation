    public void musteriGoster() {
        musteri_crud cr = new musteri_crud();
        List<db_musteri> mL = cr.listele();
        DefaultTableModel model = (DefaultTableModel) table_musteri.getModel();
        table_musteri.setFillsViewportHeight(true);
        Object[] row = new Object[4];
        for (int i = 0; i < mL.size(); i++) {
            row[0] = mL.get(i).getMusteri_id();
            row[1] = mL.get(i).getMusteri_ad();
            row[2] = mL.get(i).getMusteri_soyad();
            row[3] = mL.get(i).getMusteri_tc();
            model.addRow(row);
        }
    }

