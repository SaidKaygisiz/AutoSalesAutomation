    private void btn_gosterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_gosterActionPerformed
        tabloyu_temizle();
        DefaultTableModel model = (DefaultTableModel) table_musteri.getModel();
        DefaultTableModel model1 = (DefaultTableModel) table_arac.getModel();
        db_musteri m = new db_musteri();
        try {
            m.setMusteri_id((int) model.getValueAt(table_musteri.getSelectedRow(), 0));
        } catch (ArrayIndexOutOfBoundsException e) {
        }

        if (m.getMusteri_id() > 0) {
            List<db_arac> aL = ac.hangiMusterinin(m.getMusteri_id());
            if (aL.isEmpty() == false) {

                Object[] row = new Object[4];
                for (int i = 0; i < aL.size(); i++) {
                    row[0] = aL.get(i).getArac_id();
                    row[1] = mac.marka_bul(aL.get(i).getArac_marka_id());
                    row[2] = moc.model_bul(aL.get(i).getArac_model_id());
                    row[3] = aL.get(i).getArac_fiyat();
                    model1.addRow(row);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Müşterinin hiç aracı yok!", "Uyarı Penceresi", JOptionPane.INFORMATION_MESSAGE, i);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Lütfen Müşteri Seçiniz!", "Uyarı Penceresi", JOptionPane.INFORMATION_MESSAGE, i);
        }
    }//GEN-LAST:event_btn_gosterActionPerformed

