    public void renk_goster() {
        List<db_arac_renk> rL = rc.getList();
        DefaultTableModel model = (DefaultTableModel) tablo_renk.getModel();
        tablo_renk.setFillsViewportHeight(true);
        Object[] row = new Object[2];
        for (int i = 0; i < rL.size(); i++) {
            row[0] = rL.get(i).getRenk_id();
            row[1] = rL.get(i).getRenk_adi();
            model.addRow(row);
        }

    }

    public void tabloyu_temizle() {
        tablo_renk.setModel(new DefaultTableModel(null, new String[]{"ID", "Renk Adı"}));
        tablo_renk.getColumnModel().getColumn(0).setResizable(false);
        tablo_renk.getColumnModel().getColumn(1).setResizable(false);

    }

    public void kontrol() {

        btn_guncelle.setVisible(false);
        btn_temizle.setVisible(false);
        btn_sil.setVisible(false);
        btn_kaydet.setVisible(true);
    }

    /**
     * @param args the command line arguments
     */
    public void formu_temizle() {

        txt_renkid.setText("Read Only");
        txt_renkadi.setText(null);

    }

