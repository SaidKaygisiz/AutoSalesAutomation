    private void btn_kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kaydetActionPerformed

        r.setRenk_adi(txt_renkadi.getText().trim());
        if (!txt_renkadi.getText().equals("")) {
            rc.renk_kaydet(r);
            DefaultTableModel model = (DefaultTableModel) tablo_renk.getModel();
            Object[] row = new Object[2];
            row[1] = r.getRenk_adi();
            model.addRow(row);
            tabloyu_temizle();
            renk_goster();
            formu_temizle();
        } else {
            JOptionPane.showMessageDialog(this, "Renk Adı Alanını Boş Bırakmayın!", "Uyarı Penceresi", JOptionPane.INFORMATION_MESSAGE, i);
        }

    }//GEN-LAST:event_btn_kaydetActionPerformed

